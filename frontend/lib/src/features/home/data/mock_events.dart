/// Generates mock events scattered around the given coordinates.
List<MockEvent> generateMockEvents(double centerLat, double centerLng) {
  final random = Random(42);

  return List.generate(_eventTemplates.length, (i) {
    final t = _eventTemplates[i];
    final latOffset = (random.nextDouble() - 0.5) * 0.025;
    final lngOffset = (random.nextDouble() - 0.5) * 0.025;

    return MockEvent(
      id: '00000000-0000-4000-8000-${(i + 1).toRadixString(16).padLeft(12, '0')}',
      title: t.title,
      category: t.category,
      time: t.time,
      venue: t.venue,
      lat: centerLat + latOffset,
      lng: centerLng + lngOffset,
      attendees: t.attendees,
      isFree: t.isFree,
      friendsOnly: t.friendsOnly,
      rating: t.rating,
      timing: t.timing,
      organizerName: t.organizerName,
      organizerContact: t.organizerContact,
      organizerInstagram: t.organizerInstagram,
      organizerVerified: t.organizerVerified,
      imageUrl: 'https://picsum.photos/seed/${t.seed}/200/200',
    );
  });
}
